import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const assetDirectory = path.join(root, 'assets', 'body-map');
const outputDirectory = path.join(root, 'src', 'features', 'body-map');
const slugOverrides = {
  Back: 'lats',
  Quads: 'quadriceps',
  Trapezius: 'traps',
  Lower_Back: 'lower_back',
  Delta: 'shoulders'
};
const definitions = {
  front: {
    file: 'front.svg',
    groups: {
      Chest: ['CHEST', 'Chest'], Obliques: ['OBLIQUES', 'Obliques'], Abs: ['ABS', 'Abdominals'],
      Biceps: ['BICEPS', 'Biceps'], Quads: ['QUADRICEPS', 'Quadriceps'],
      Trapezius: ['TRAPS', 'Upper trapezius'], Shoulders: ['SHOULDERS', 'Shoulders'],
      Forearms: ['FOREARMS', 'Forearms'], Abductors: ['ABDUCTORS', 'Hip abductors'],
      Adductors: ['ADDUCTORS', 'Hip adductors']
    }
  },
  back: {
    file: 'back.svg',
    groups: {
      Back: ['LATS', 'Lats'], Triceps: ['TRICEPS', 'Triceps'], Forearms: ['FOREARMS', 'Forearms'],
      Obliques: ['OBLIQUES', 'Obliques'], Trapezius: ['TRAPS', 'Trapezius'],
      Lower_Back: ['LOWER_BACK', 'Lower back'], Delta: ['SHOULDERS', 'Rear shoulders'],
      Glutes: ['GLUTES', 'Glutes'], Calves: ['CALVES', 'Calves'], Hamstrings: ['HAMSTRINGS', 'Hamstrings']
    }
  }
};

function parsePaths(svg, view, groups) {
  const stack = [];
  const counts = new Map();
  const paths = [];
  for (const line of svg.split(/\r?\n/)) {
    const group = line.match(/<g\b[^>]*\bid="([^"]+)"[^>]*>/);
    if (group) stack.push(group[1]);
    const pathMatch = line.match(/<path\b[^>]*\bd="([^"]+)"[^>]*\/>/);
    if (pathMatch) {
      const groupName = [...stack].reverse().find((name) => groups[name]);
      if (groupName) {
        const index = counts.get(groupName) ?? 0;
        counts.set(groupName, index + 1);
        const absSides = ['center-lower', 'left-upper', 'left-middle', 'left-lower', 'right-upper', 'right-middle', 'right-lower'];
        const isBackCenter = view === 'back' && (groupName === 'Trapezius' || groupName === 'Lower_Back');
        const side = groupName === 'Abs'
          ? absSides[index]
          : isBackCenter
            ? 'center'
            : index === 0 ? 'left' : index === 1 ? 'right' : `part-${index + 1}`;
        const slug = slugOverrides[groupName] ?? groupName.toLowerCase();
        paths.push({
          id: `${view}_${slug}_${side.replaceAll('-', '_')}`,
          view,
          side,
          label: groups[groupName][1],
          muscleGroup: groups[groupName][0],
          d: pathMatch[1]
        });
      }
    }
    const closeCount = (line.match(/<\/g>/g) ?? []).length;
    if (closeCount) stack.splice(Math.max(0, stack.length - closeCount), closeCount);
  }
  return paths;
}

function createOverlaySvg(paths) {
  const body = paths.map((item) =>
    `  <path id="${item.id}" data-muscle="${item.muscleGroup}" data-label="${item.label}" data-side="${item.side}" d="${item.d}"/>`
  ).join('\n');
  return `<?xml version="1.0" encoding="UTF-8"?>\n<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 600 1220">\n${body}\n</svg>\n`;
}

const allPaths = [];
await mkdir(outputDirectory, { recursive: true });
for (const [view, definition] of Object.entries(definitions)) {
  const svg = await readFile(path.join(assetDirectory, definition.file), 'utf8');
  const paths = parsePaths(svg, view, definition.groups);
  allPaths.push(...paths);
  await writeFile(path.join(assetDirectory, `${view}.overlay.svg`), createOverlaySvg(paths));
}
const generated = `// Generated by scripts/generate-body-map.mjs. Do not edit manually.\nimport type { BodyMapPath } from './body-map-types';\n\nexport const BODY_MAP_PATHS = ${JSON.stringify(allPaths, null, 2)} as const satisfies readonly BodyMapPath[];\n`;
await writeFile(path.join(outputDirectory, 'body-map-paths.generated.ts'), generated);
console.log(`Generated ${allPaths.length} body-map paths without geometry transforms.`);
